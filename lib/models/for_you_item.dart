class ForYouItem {
  final String imageUrl;
  final String title;
  final String creator;
  final int friendsCount;
  final String date;
  final String time;
  final String location;

  ForYouItem({
    required this.imageUrl,
    required this.title,
    required this.creator,
    required this.friendsCount,
    required this.date,
    required this.time,
    required this.location,
  });
}
